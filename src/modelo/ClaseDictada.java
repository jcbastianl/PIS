package modelo;

/**
 *
 * @author jsbal
 */
public class ClaseDictada {
    private Integer id;
    private String tema;
    private Integer id_cursa;
    private Integer id_asignatura;

    public ClaseDictada() {
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the tema
     */
    public String getTema() {
        return tema;
    }

    /**
     * @param tema the tema to set
     */
    public void setTema(String tema) {
        this.tema = tema;
    }

    /**
     * @return the id_cursa
     */
    public Integer getId_cursa() {
        return id_cursa;
    }

    /**
     * @param id_cursa the id_cursa to set
     */
    public void setId_cursa(Integer id_cursa) {
        this.id_cursa = id_cursa;
    }

    /**
     * @return the id_asignatura
     */
    public Integer getId_asignatura() {
        return id_asignatura;
    }

    /**
     * @param id_asignatura the id_asignatura to set
     */
    public void setId_asignatura(Integer id_asignatura) {
        this.id_asignatura = id_asignatura;
    }
    
    
}
